/**
 * Builds a gzip-compressed lat/lon ring index for point-in-polygon lookups.
 * Run: node scripts/build-location-data.js
 */
const fs = require('fs');
const path = require('path');
const zlib = require('zlib');

const INPUT = path.join(__dirname, '../data/departements.geojson');
const OUTPUT = path.join(__dirname, '../data/departements-location.compressed.js');

/** ~90 m simplification in degrees (enough for 300 m GPS tolerance). */
const LOCATION_TOLERANCE = 0.0008;

const perpendicularDistance = (point, lineStart, lineEnd) => {
  const [x, y] = point;
  const [x1, y1] = lineStart;
  const [x2, y2] = lineEnd;
  const dx = x2 - x1;
  const dy = y2 - y1;

  if (dx === 0 && dy === 0) {
    return Math.hypot(x - x1, y - y1);
  }

  return Math.abs(dy * x - dx * y + x2 * y1 - y2 * x1) / Math.hypot(dx, dy);
};

const douglasPeucker = (points, tolerance) => {
  if (points.length <= 2) {
    return points;
  }

  let maxDistance = 0;
  let index = 0;

  for (let i = 1; i < points.length - 1; i += 1) {
    const distance = perpendicularDistance(points[i], points[0], points[points.length - 1]);
    if (distance > maxDistance) {
      maxDistance = distance;
      index = i;
    }
  }

  if (maxDistance > tolerance) {
    const left = douglasPeucker(points.slice(0, index + 1), tolerance);
    const right = douglasPeucker(points.slice(index), tolerance);
    return left.slice(0, -1).concat(right);
  }

  return [points[0], points[points.length - 1]];
};

const roundCoord = (value) => Math.round(value * 100000) / 100000;

const simplifyRing = (ring) => {
  const simplified = douglasPeucker(ring, LOCATION_TOLERANCE);
  if (simplified.length < 4) {
    return ring.map(([lon, lat]) => [roundCoord(lon), roundCoord(lat)]);
  }
  return simplified.map(([lon, lat]) => [roundCoord(lon), roundCoord(lat)]);
};

const geometryToRings = (geometry) => {
  if (geometry.type === 'Polygon') {
    return geometry.coordinates.map(simplifyRing);
  }
  if (geometry.type === 'MultiPolygon') {
    return geometry.coordinates.flatMap((polygon) => polygon.map(simplifyRing));
  }
  return [];
};

if (!fs.existsSync(INPUT)) {
  console.error('Missing departements.geojson — run npm run build:map-data first.');
  process.exit(1);
}

const geojson = JSON.parse(fs.readFileSync(INPUT, 'utf8'));
const departments = [];

for (const feature of geojson.features) {
  const rings = geometryToRings(feature.geometry);
  if (!rings.length) {
    continue;
  }
  departments.push({
    c: feature.properties.code,
    r: rings,
  });
}

const payload = JSON.stringify({ d: departments });
const compressed = zlib.gzipSync(payload, { level: 9 }).toString('base64');
const fileContents = `// Generated by scripts/build-location-data.js — do not edit
export default '${compressed}';
`;

fs.writeFileSync(OUTPUT, fileContents);

console.log(
  `Wrote ${departments.length} department location indexes (${(payload.length / 1024).toFixed(0)} KB raw, ${(Buffer.byteLength(fileContents, 'utf8') / 1024).toFixed(0)} KB bundle)`
);
